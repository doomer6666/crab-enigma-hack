from typing import List


BASE_MESSAGES = {
    "Неисправность": "Вы столкнулись с технической неисправностью оборудования.",
    "Калибровка": "Ваш запрос связан с калибровкой оборудования.",
    "Запрос документации": "Вы запрашиваете техническую документацию.",
    "Подключение и ПО": "Ваш вопрос связан с подключением или программным обеспечением.",
    "Запрос пароля": "Ваш запрос касается восстановления доступа.",
    "Монтаж и установка": "Ваш вопрос связан с монтажом или установкой оборудования.",
    "Гарантия и ремонт": "Ваш запрос касается гарантийного обслуживания или ремонта.",
    "Другое": "Спасибо за ваше обращение."
}


def generate_reply(category: str, kb_context: List[str]) -> str:

    category_block = BASE_MESSAGES.get(category, BASE_MESSAGES["Другое"])

    kb_block = (
        "\n".join(kb_context)
        if kb_context
        else "Рекомендуем уточнить детали вашего запроса."
    )

    return f"""
Здравствуйте!

Спасибо за ваше обращение.

{category_block}

Рекомендуемое решение:
{kb_block}

Если проблема сохраняется — пожалуйста, сообщите дополнительные детали.

С уважением,
Техническая поддержка
""".strip()